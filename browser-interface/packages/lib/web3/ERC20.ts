export interface IERC20 extends Contract {
  name(options?: Partial<TransactionOptions>): Promise<string>
  totalSupply(options?: Partial<TransactionOptions>): Promise<BigNumber>
  approve(_spender: Address, _value: BigNumber, options?: Partial<TransactionOptions>): Promise<boolean>
  transferFrom(_from: Address, _to: Address, _value: BigNumber, options?: Partial<TransactionOptions>): Promise<boolean>
  decimals(options?: Partial<TransactionOptions>): Promise<number>
  balanceOf(_of: Address, options?: Partial<TransactionOptions>): Promise<BigNumber>
  symbol(options?: Partial<TransactionOptions>): Promise<string>
  transfer(_to: Address, _value: BigNumber, options?: Partial<TransactionOptions>): Promise<boolean>
  allowance(_owner: Address, _spender: Address, options?: Partial<TransactionOptions>): Promise<BigNumber>
}

// TODO: Cache contracts by address+requestManager, contracts are immutable

export async function getERC20(requestManager: RequestManager, address: Address): Promise<IERC20> {
  return (await new ContractFactory(requestManager, ERC20Abi).at(address)) as IERC20
}
